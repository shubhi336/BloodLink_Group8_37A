public void submitStory(){
    String storyText=storyArea.getText().trim();
    
    if (storyText.isEmpty()){
        JOptionPane.showMessageDialog(this, "Please write your story before submitting.");
        return;
    }
    Story story=new Story (email,storyText);
    StoryDao dao=new StoryDao();
    boolean success=dao.saveStroy(story);
    
    if (success){
        JOptionPane.showMessageDialog(this, "Your story has been shared. Thank you");
        storyArea.setText("");
    }else{
        JOptionPane.showMessageDialog(this, "Failed to share your story. Try again later.");
    }
}

