    private void Add_ButtonActionPerformed(java.awt.event.ActionEvent evt) {                                           
        // TODO add your handling code here:
        String name = Name_Field.getText();
        String day = Day_Field.getText();
        String location = Location_Field.getText();

        SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
        String date = sdf.format(Date_Field.getDate());

        boolean success = CampaignDao.insertCampaign(name, day, date, location);
        if (success) {
            JOptionPane.showMessageDialog(this, "Campaign added successfully.");
            Name_Field.setText("Campaign name");
            Day_Field.setText("Day");
            Location_Field.setText("Location");
            Date_Field.setDate(null);
            loadCampaigns();
        } else {
            JOptionPane.showMessageDialog(this, "Failed to add campaign.");
        }
    }

    private void loadCampaigns() {
        jPanel4.removeAll();
        jPanel4.setLayout(new BoxLayout(jPanel4, BoxLayout.Y_AXIS));

        List<Campaign> campaigns = CampaignDao.getAllCampaigns();
        for (Campaign campaign : campaigns) {
            JPanel panel = new JPanel();
            panel.setLayout(new BoxLayout(panel, BoxLayout.Y_AXIS));
            panel.setBorder(BorderFactory.createLineBorder(Color.GRAY));
            panel.setBackground(new Color(240, 240, 240));
            panel.setMaximumSize(new Dimension(800, 120));

            JLabel nameLabel = new JLabel("Campaign Name: " + campaign.getName());
            JLabel dayLabel = new JLabel("Day: " + campaign.getDay());
            JLabel dateLabel = new JLabel("Date: " + campaign.getDate());
            JLabel locationLabel = new JLabel("Location: " + campaign.getLocation());

            JButton deleteButton = new JButton("Delete");
            deleteButton.setBackground(Color.RED);
            deleteButton.setForeground(Color.WHITE);
            deleteButton.addActionListener(e -> {
                int confirm = JOptionPane.showConfirmDialog(this, "Delete this campaign?", "Confirm", JOptionPane.YES_NO_OPTION);
                if (confirm == JOptionPane.YES_OPTION) {
                    if (CampaignDao.deleteCampaignById(campaign.getId())) {
                        JOptionPane.showMessageDialog(this, "Campaign deleted successfully.");
                        loadCampaigns();
                    } else {
                        JOptionPane.showMessageDialog(this, "Failed to delete campaign.");
                    }
                }
            });

            panel.add(nameLabel);
            panel.add(dayLabel);
            panel.add(dateLabel);
            panel.add(locationLabel);
            panel.add(deleteButton);
            jPanel4.add(panel);
            jPanel4.add(Box.createVerticalStrut(10));
        }

        jPanel4.revalidate();
        jPanel4.repaint();
    }

